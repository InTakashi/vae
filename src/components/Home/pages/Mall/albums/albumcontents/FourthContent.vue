        <p>07. Play with style</p>
        <p>何谓Play with style？抛开潮流的诱惑，突破名相的束缚，活出真自我，找到自己的态度是为先！此作品除了歌词层面传达了这一意旨，音乐方面也极具先锋性：世界民族乐器的大量融入，给音乐增添了一份神秘的色彩——但却又辅以西洋感的R&B鼓点、极前卫的弹性演唱方式——这种古今碰撞所产生的特殊质感，恐怕只有许嵩能独家定制。</p>
        <p>08. 心疼你的过去</p>
        <p>「 忽然你哭了 / 问你在哭什么 / 你哭的更狠了 / 我好像明白了 」</p>
