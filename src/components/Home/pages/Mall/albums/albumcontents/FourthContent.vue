        <p>你若爱Ta，你便很可能纠结于Ta的过去；Ta若爱你，便很可能对你隐瞒Ta的过去。</p>
        <p>如果时间是一条河……你是否愿意在梦里游回到岁月上游，去更早的时间点和Ta相遇，为Ta拦下所有的伤害？</p>
        <p>许嵩此作采用日记体写作风格，整篇没有任何诗意斧凿，用最真实的笔触把最纠结的心疼刻画到血肉里，令人惊心。循环渐进式的R&B Loop编曲与深情款款的念唱，逐层铺排出幽诡阴郁的氛围。从《伴虎》的激越磅礴，到《对话老师》的顽皮十足，再到《幻听》的传统深情，直到这一首的Rap……许嵩作为创作人以及演唱者，在《梦游计》这张专辑中展现了对多种曲风的极佳驾驭力，使得此专辑一经推出无论在广度还是深度上都备受瞩目。</p>
        <p>09. 全球变冷</p>
        <p>这是一个全球化的时代，这是一个地球村。但不知道从什么时候开始，繁盛的背后培育起冷漠，GDP的上扬伴随着道德的滑坡，丰富的物质世界背后是精神世界的逐渐荒芜……我们是该坐视人与人之间冰山横陈、任凭全球变冷，还是用微笑温暖彼此？许嵩此作写于“小悦悦”事件之时，不为问责，只愿同样的悲剧能少一些，人与人之间的关怀能多一些。</p>
        <p>10. 亲情式的爱情</p>
        <p>「 我有多想多想再看一眼 / 你最后那天在我身边 / 那熟悉的脸 ……」</p>
        <p>是否电光石火般的爱恋，最终都会归于平淡？当曾经的日思夜想辗转反侧魂不守舍，沦为一起吃饭睡觉买菜吵架做家务，恐怕就再也没有给对方写一封情书的雅兴了……</p>
        <p>但，爱是消失了，还是悄悄转化了存在形式？</p>
        <p>许嵩这首作品的创作，是感于新闻里一件真人真事。一对中年夫妻遭遇了一场严重车祸，丈夫卡在车轮下难以救出，他停止心跳前对救援人员的最后一句话是，别管我，先救我爱人！说完便再也没有睁开眼睛。侥幸生还的妻子在事后哭着接受采访说，就在车祸发生前一分钟，他们还在为家庭琐事吵嘴，吵得很凶，而前一阵还在闹离婚。……</p>
        <p>珍惜身边人，让爱从日常生活里流淌出来，不要等到失去才追悔，一辈子并没有想象之中那么长。</p>
