        <p>许嵩，85后原创音乐人，2009年独立出版首张词曲全创作专辑《自定义》，2010年独立出版第二张词曲全创作专辑《寻雾启示》，两度掀起讨论热潮，并均在唱片发行一个月内售罄。</p>
