        <p>思考者的梦</p>
        <p>『苏格拉底：未经思考的生活是不值得过的』</p>
        <p>许嵩要发片了。在这段不算短的日子中，欣赏许嵩的人们，仅能透过市面上流传属于许嵩气息的旋律，来稍稍缓和期盼的心情。经过一年的沉淀，许嵩第三张词曲全创专辑---「苏格拉没有底」终于在乐迷们期待之下，定于2011年4月1日面世。</p>
        <p>从昔日那个写着哼着《城府》夺得各大音乐试听榜冠军的音乐顽童成长到今时，许嵩在音乐层面一直保持独立做派，此番再度包揽十首词曲的全部创作。毫无粉饰的生存哲学，超越现实的想象思辨，细而不腻的私人回忆，在十首作品里尽数展现。自成一派的制作风格与词曲质感，随性慵懒的演绎方式，标志性的假音与转音，舒服惬意的中慢板R&B节奏……令人一听便可品出强烈的个人风味。浪漫与幽默并存、务实与务虚交织，《苏格拉没有底》仿若暗器般无色无味却能迅速在人心中稳固沉积，在悠然自得的节奏摆荡中持续扩散。</p>
        <p>《想象之中》</p>
        <p>这首慢板R&B作品，内敛中带些慵懒的唱腔——无论是走在街头，坐在车里，临睡前，对着电脑时……听起来都很惬意。作为这张专辑的总制作人，许嵩就是有这样一种能力，让人轻易就跟着他的安排陷入思考或回忆。当然，这与他虽年纪轻轻就已全权制作过两张专辑及十余首热门单曲的经验有莫大关系。《想象之中》由一个诡异中带点滑稽感的电子音色开场，这种阴郁懒散的情感基调，已成为他的个人特质。大气却不乏细腻之处的旋律，值得玩味的歌词，保障了该作品的一线水准。单论演唱而言，许嵩亦有一种独特的声线气质：慵懒中仍保持着清新的气息。大量滑音、装饰音穿插在整首作品中，但却也不会给人以油滑之感。至于情绪的分寸拿捏也相当到位：3分37秒，沙哑的一瞬，情绪的涌动把伤感推到新的高度。</p>
        <p>《河山大好》</p>
        <p>像是一种童趣的重返，他从来都不是一个只会苦着脸唱悲歌的忧郁人，他的喜与悲都在歌里了。他的作品情绪向来是立体、多面的。纵观整张专辑，十首风格迥异的词曲均出自一人之手，这份真实与挥洒，让作者的形象变得愈发清晰可爱。《河山大好》似乎有劝年轻人莫要宅的含义，但却没有半点说教之感，而是如同一个老友，邀你一起出门走走。“保持浪漫心态，活着就不算坏”与其说是一种情绪，不如说是一种情怀；“地球离了谁它都照样公转自转”，谁都别把自己太当回事，这种论调就更洒脱。不落俗套的旋律，清新的念唱，R&B的节奏骨架，构建起了整首歌的律动，让人情不自禁跟着节奏摆荡，有“出去走走”的冲动。</p>
        <p>《拆东墙》</p>
        <p>词面上，许嵩借叙述一个唐代小酒馆老板的悲情人生，影射当下种种社会现实，颇有后现代意味。没有愤怒、没有发泄，而是以淡淡的悲悯之情，让诸多尖锐话题拥有了新的探讨方式与出口，称得上是惊艳之作。从大局而言，起承转合收放自如；细节上，黑色幽默与巧思不断，许嵩的笔风独树一帜。如果说这种“借古喻今”还称不上是创举，那么全曲的最后一句则把这首作品引入哲学思考的范畴。至于音乐部分，念唱流畅如水；R&B吉他丝丝入扣；副歌的旋律与歌词咬合度超高。词、曲、唱、制作出自一人，且如这般天然合一，当下恐怕没有几人做得到了。</p>
        <p>《医生》</p>
        <p>“我也没那种，庸俗的情结”。不知许嵩暗指的是哪种情结，但也正基于这种朦胧，这首作品的疗伤范围似乎变得更大了。成熟厚重的音乐，初时让人不解：为什么许嵩在这首歌的表达上要如此传统保守？读着歌词渐渐明白了：爱情世界里的一个老实人，他的保守的内在，他为对方疗伤的决心，他此地无银三百两的说自己没那种庸俗的情结……让人的心有被揪了一下的痛感。以如此的曲风呈现如此的歌词内容，也是一种恰如其分吧。</p>
        <p>《微博控》</p>
        <p>科技的浪潮也注定了越来越多的人会成为那种“不玩微博就觉得少了点什么”的微博控。而许嵩对生活的观察依然敏锐，对热门事物的理解依然独到。《微博控》透过描写一对相互都玩微博的恋人，传达了感情在网络交织的叶片上有趣的脉络。而副歌更是隐含了对“做的多不如说得多”的社会现状的思考。把创作深度藏匿于简单的歌词背后，是许嵩从作品《素颜》里就呈现了的另一路特质。至于韩范儿的R&B曲风，弹性十足的俏皮演绎，都是这首歌的加分项。</p>
        <p>《毁人不倦》</p>
        <p>“几片来自松岛的枫叶”……作为整张专辑中最冷艳的一首作品，作词、作曲、编曲、混音连同录音均由许嵩一人包办。阴郁空灵的编曲，让人印象颇深。在隐晦的歌词中，自嘲、批判、痛苦重叠交织，他骨子里的反叛由此窥见。</p>
        <p>《双人旁》</p>
        <p>简简单单的抒情作品，回归原始的感动。人生在世，每个人的身旁都有或有过那么一个爱的人，那种温暖，是每个人都乐于回味享受的。90’s R&B风格，简约而怀旧；作者使出了很萌的唱法，让这首歌的气质跳脱于整张专辑的冷色调。难得的温馨流露。</p>
        <p>《降温》</p>
        <p>这首作品没有“歌词”，而是一则唱出来的“日记”。真实感极强的词句，记录2010年11月20日的生活细节；从多处貌似无心插柳的“设局”，不难揣测此篇日记的良苦用心。念唱式的旋律层层递进，带点儿混搭感的三弦与京味儿独白——作为一首实验之作，它不是徒有其表。而结尾一句突然跳转画面，实谓神来之笔。</p>
        <p>《敬酒不吃》</p>
        <p>当作为生存本能的吃与喝被利益目的附身——不是所有人都愿意接受这套规则。许嵩把社会生活中的极微小画面做艺术放大，相当犀利。带点爵士味的R&B，清冷凛冽；独特的真假声转换，宣称着“我就这副死样”……通过这首作品，看不到许嵩的努力，只看到许嵩的天资。因为，这样的作品绝不是靠“用心写”就能写出的——如果骨子里没有那种把音乐当成玩儿的洒脱劲的话。</p>
        <p>《千百度》</p>
        <p>当中国风的歌被写滥，人们都在奇怪为什么除了台湾的周杰伦，能把中国风写红唱红的就只剩许嵩了？中国风可不是用几个民乐器夹杂几个半文不白的句子就能红。周杰伦有方文山，而许嵩只有许嵩，如《清明雨上》、《半城烟沙》、《庐州月》等等口碑之作，全部出自许嵩笔下。典故的化用，创造新词汇的想象力，借古喻今的手法，传统戏曲技巧的融入——他自有他自己的一套。在《千百度》里，温婉内敛与开阔大格局兼容并蓄，这是许嵩独有的音乐气质，换个人唱就没有这个味了。其实纵观专辑里的每一首作品，都具备这种“非他莫属”的专属质感。</p>
        <p>MV全新视觉</p>
        <p>『苏格拉底：每个人身上都有太阳，主要是如何让它发光』</p>
        <p>许嵩2011思想大碟之主打曲目《想象之中》《河山大好》《拆东墙》，特邀三位金曲奖mv导演黄中平、徐仁峰、陈映之倾力打造影像世界。许嵩此番系首次拍摄MV，首度尝试在影像中呈现自己的作品。虽然起初难免青涩，但很快就和各方前辈打成一片、默契配合，公司同事也笑其之慢热。</p>
        <p>拍摄《想象之中》MV并身兼本专辑摄影师的黄中平导演可谓华语乐坛MV导演巨匠。他的作品画面唯美，对美学的苛求达到超乎想象的地步。影像的极简化风格趋向，散发出优雅的气质。许嵩在该支MV里的真情演出，特别是一场雨中舞蹈拥抱的场景，让现场所有人为之触动。一场雨，一支笛，一盆枯树……这支mv如果被读懂，绝对令人动容。</p>
        <p>《河山大好》由鬼才导演徐仁峰掌舵，他历来以想法新奇而著称，这一特点与许嵩的音乐风格不谋而合。此番徐导和许嵩一起连袂呈现奇幻异想世界，mv的画面暗喻颇让人玩味。在台北西门町这个青少年流行圣地拍摄时，引发众多年轻人围观。</p>
        <p>《拆东墙》则出自陈映之这位新锐导演。与许嵩同归80后创意人的他，2010年第21届金曲奖凭借苏打绿《春·日光》一举夺得最佳音乐录像带奖。他的MV细腻独到，快歌充满活跃气息，而慢歌有让人回味的唯美。这首《拆东墙》所蕴含的哲学意味，经由这支mv便可窥探一番。</p>
        <p>愚人节的诚意</p>
        <p>2011，春夏交替，距离人们戏谑的所谓世界末日又近了一些。比起这个依旧好吃好笑好玩好闹的世界，乏味的华语音乐世界似乎急需一桌耐吃的家常菜——营养高，饱含诚意。无需用很大的餐盘，更无需在餐盘边儿配一朵花——《苏格拉没有底》便是这样一张无需粉饰的诚意之作。</p>
