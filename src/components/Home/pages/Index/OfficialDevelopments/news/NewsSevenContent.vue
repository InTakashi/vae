      <span>许嵩：1986年生于安徽合肥，歌手、创作人、制作人。毕业于安徽医科大学，2006年在网络上发布音乐作品，2009年发行首张专辑《自定义》。2021年5月，发行第八张专辑《呼吸之野》。</span>
